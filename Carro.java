public class Carro {

    double combustivel;
    double peso;
    String marca;
    int anoFabricacao;

    void setCombustivel(double combustivel) {
        this.combustivel = combustivel;
    }

    double getCombustivel() {
        return this.combustivel;
    }

    void setPeso(double peso) {
        this.peso = peso;
    }

    double getPeso() {
        return this.peso;
    }

    void setMarca(String marca) {
        this.marca = marca;
    }

    String getMarca() {
        return this.marca;
    }

    void setAnoFabricacao(int anoFabricacao) {
        this.anoFabricacao = anoFabricacao;
    }

    int getAnoFabricacao() {
        return this.anoFabricacao;
    }
    String tipo;
    String cor;
    String placa;
    int numPortas;

    void setCor(String cor) {
        this.cor = cor;
    }

    String getCor() {
        return this.cor;
    }

    void setTipo(String tipo) {
        this.tipo = tipo;
    }

    String getTipo() {
        return this.tipo;
    }

    void setPlaca(String placa) {
        this.placa = placa;
    }

    String getPlaca() {
        return this.placa;
    }

    void setNumPortas(int numPortas) {
        this.numPortas = numPortas;
    }

    int getNumPortas() {
        return this.numPortas;
    }
}

